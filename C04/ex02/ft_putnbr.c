#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	long	temp;

	temp = nb;
	if (nb < 0)
	{
		ft_putchar('-');
		temp = -temp;
	}
	if (temp > 9)
	{
		ft_putnbr(temp / 10);
		ft_putchar((char)((temp % 10) + '0'));
	}
	else
	{
		ft_putchar((char)(temp + '0'));
	}
}

