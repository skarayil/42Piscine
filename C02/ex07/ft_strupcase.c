char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] += 'A' - 'a';
		i++;
	}
	return (str);
}


int	main(void)
{
	char	str[] = "all is lowercase";

	printf("Before: %s\n", str);
	printf("After: %s\n", ft_strupcase(str));
} 