#include <unistd.h>

void	ft_print_numbers(void)
{
	char	harf;

	harf = '0';
	while (harf <= '9')
	{
		write(1, &harf, 1);
		harf++;
	}
}


int	main(void)
{
	ft_print_numbers();
}