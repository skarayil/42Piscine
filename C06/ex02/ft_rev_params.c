#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	char	*param;

	i = argc - 1;
	while (i > 0)
	{
		param = argv[i];
		while (*param)
		{
			write(1, param, 1);
			param++;
		}
		write(1, "\n", 1);
		i--;
	}
	return (0);
}
